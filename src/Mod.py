# Ansi Color codes for colored console printouts
class AnsiColor:
    HEADER = '\033[95m'     # Font Size
    OKBLUE = '\033[94m'     # Blue
    OKCYAN = '\033[96m'     # Cyan
    OKGREEN = '\033[92m'    # Green
    WARNING = '\033[93m'    #
    FAIL = '\033[91m'       # 
    ENDC = '\033[0m'        # Default (Usually White)
    BOLD = '\033[1m'        # Font Style
    UNDERLINE = '\033[4m'   # Font Style


